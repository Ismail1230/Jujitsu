<h2>What are We?</h2>
<p>We are a martial arts international academy based in Mauritius at Mesnil.
Founded in 1986 by Hanshi Sorab Mowlabocus,who is currently an 8th Dan,the
academy has produced over 15 qualified black belt teachers(senseis) who are recognised
internationally and are affliated with the Jujitsu World Kobudo Federation.Besides training
in the dojo, we also organise outdoor trainings,parent/teacher meetings and parties as well.
</p>
<p>To upgrade to a higher rank, students have to earn it by attending the trainings regularly,
performing well in techniques and building up their stamina. These factors are carefully monitored
and when a student is ready, Hanshi will allow him to take up an exam for the next rank.
</p>
<p>Note that any student as from the age of five can join the academy. As for the age limit,
we do not have any because we believe that it is never too late for somebody to learn martial
arts, no matter at what age they are!</p>
