		<h2>Why choose Martial arts as a sport?</h2>
		
	    <h5>Self-Defense</h5>
		<p>Majority of those signing up for martial arts have personal safety
           as their main reason for training.Violence is a reality that is faced 
		   by everyone and martial arts training provides necessary skills to
		   increase chances of survival.Students learn and practice risk avoidance
		   and fighting skills that they can apply should their safety be at risk.
		</p>
		
		<h5>Self-Confidence</h5>
		<p>Fear and lack of knowledge and skill contribute largely to poor self confidence.
		   Martial arts training provides tools that enable people to learn and apply skills
		   that help them realize not only what they are capable of but also gives them a sense
		   of pride from knowing what they have accomplished. Fear is reduced from knowing 
           there are skills to draw upon when attacked.As students progress to new levels, challenges
		   challenges are met with less apprehsion and with more drive to perservere and succeed.
		</p>
		
		<h5>Community</h5>
		<p>Martial arts in itself is a community. While martial arts was first used by people
	       to secretly train together to fight against oppressors, today’s martial arts has
		   become a community with an even bigger purpose. It’s where people meet and share 
		   with others their passions.It’s where you find people of all ages and from all 
		   walks of life to learn from and pass on knowledge. It’s where people train hard, 
		   celebrate the wins and happy moments and comfort in times of trouble and loss.
		   It is where people can improve themselves and where they can be of service to others.
